console.log(2 > 1); // greater
console.log(2 >= 1); // greater then equal to
console.log(2 < 1); // lesser 
console.log(2 <= 1); // lesser then equal to
console.log(2 == 1); // equal to but 2 == "2" -> true
console.log(2 === 1); //equal to but 2 === "2" -> false 
console.log(2 !== "2"); // Not equal to but 2 !== "2" -> true
console.log(2 != "2"); // Not equal to but 2 != "2" -> false